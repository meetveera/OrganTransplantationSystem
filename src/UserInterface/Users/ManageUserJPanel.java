    private void createUserJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserJButtonActionPerformed
        String userName = UserNameJTxtField.getText();
        String password = passwordJTextField.getText();
        Organizations organization = (Organizations) organizationJComboBox.getSelectedItem();
        User user = (User) employeeJComboBox.getSelectedItem();
        Role role = (Role) roleJComboBox.getSelectedItem();

        String PATTERN= "^[a-zA-Z0-9_]{0,30}[@][a-zA-Z0-9]{0,10}[.][a-zA-Z]{0,10}$";
        Pattern patt=Pattern.compile(PATTERN);
        Matcher match= patt.matcher(UserNameJTxtField.getText());
        if(UserNameJTxtField.getText().isEmpty() || passwordJTextField.getText().isEmpty()){
        
            JOptionPane.showMessageDialog(null,"Please enter the username and password!");
            return;
        }
        
        else if(!match.matches()){
            JOptionPane.showMessageDialog(null,"Please enter a proper email address");
            return;
        }
         else {
            for(UserAccount u:organization.getUserAccountDirectory().getUserAccounts()){
                        if(UserNameJTxtField.getText().equalsIgnoreCase(u.getUsername())){
                            JOptionPane.showMessageDialog(null,"This User Name Already Exists !");
                            return;
                        }
                        
}
           
        }
        organization.getUserAccountDirectory().createUserAccount(userName, password, user, role);

        popData();
        JOptionPane.showMessageDialog(null,"User registered successfully!");
        UserNameJTxtField.setText("");
        passwordJTextField.setText("");
        
         
    }//GEN-LAST:event_createUserJButtonActionPerformed

