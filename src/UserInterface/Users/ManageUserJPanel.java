    public void popOrganizationComboBox() {
        organizationJComboBox.removeAllItems();

        for (Organizations organization : enterprise.getOrganizationdir().getOrganizationList()) {
            organizationJComboBox.addItem(organization);
        }
    }
    public void populateEmployeeComboBox(Organizations organization){
        employeeJComboBox.removeAllItems();
        
        for (User user : organization.getUserDirectory().getUserList()){
            employeeJComboBox.addItem(user);
        }
    }

    private void populateRoleComboBox(Organizations organization){
        roleJComboBox.removeAllItems();
        for (Role role : organization.getSupportedRole()){
            roleJComboBox.addItem(role);
        }
    }
    
    public void popData() {

        DefaultTableModel model = (DefaultTableModel) userJTable.getModel();

        model.setRowCount(0);

        for (Organizations organization : enterprise.getOrganizationdir().getOrganizationList()) {
            for (UserAccount ua : organization.getUserAccountDirectory().getUserAccounts()) {
                Object row[] = new Object[2];
                row[0] = ua;
                row[1] = ua.getRole();
                ((DefaultTableModel) userJTable.getModel()).addRow(row);
            }
        }
    }
