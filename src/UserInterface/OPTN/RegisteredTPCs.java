    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseJTable.getModel();

        model.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            for (Enterprise enterprise : network.getEp().getEnterpriselist()) {
                for (UserAccount userAccount : enterprise.getUserAccountDirectory().getUserAccounts()) {
                    Object[] row = new Object[3];
                    row[0] = enterprise.getName();
                    row[1] = network.getName();
                    row[2] = userAccount.getUsername();

                    model.addRow(row);
                }
            }
        }
    }
    private void populateNetworkcombobox()
    {
        Networkscombobox.removeAllItems();
        EnterpriseType.removeAllItems();
        for(Network network:system.getNetworkList())
        {
            Networkscombobox.addItem(network);
            
        }
        for (Enterprise.EnterpriseType type : Enterprise.EnterpriseType.values()) {
            EnterpriseType.addItem(type);
            
        }
       
        
        
        
    }
    

