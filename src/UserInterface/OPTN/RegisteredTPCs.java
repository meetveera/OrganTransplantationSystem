    private void RegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterActionPerformed
        // TODO add your handling code here:
       
        if(nameTextField.getText().isEmpty() || UserNameTextField.getText().isEmpty() || Password.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Please fill in all the details");
        }
        else if(!Password.getText().equals(RePassword.getText())){
            JOptionPane.showMessageDialog(null,"Passwords no not match");
            return;
        }
        else{
        Enterprise e=(Enterprise)Enterprisecombobox.getSelectedItem();
        String username = UserNameTextField.getText();
        String password = String.valueOf(Password.getPassword());
        String name = nameTextField.getText();
        if(e==null)
        {
            JOptionPane.showMessageDialog(null,"Please select a valid Enterprise");
            return;
        }
        User user = e.getUserDirectory().createUser(name);
        String er = EnterpriseType.getSelectedItem().toString();
        if((EnterpriseType.getSelectedItem().toString()).equalsIgnoreCase("Transplantation Center")){
                for(UserAccount ua:e.getUserAccountDirectory().getUserAccounts()){
                    if(ua.getUsername().equals(username)){
                        JOptionPane.showMessageDialog(null,"This User Name Already Exists !");
                        return;
                    }                   
                }
            UserAccount account = e.getUserAccountDirectory().createUserAccount(username, password, user, new AdminRole());
        }
        else if((EnterpriseType.getSelectedItem().toString()).equalsIgnoreCase("User")){
            for(UserAccount ua:e.getUserAccountDirectory().getUserAccounts()){
                    if(ua.getUsername().equals(username)){
                        JOptionPane.showMessageDialog(null,"This User Name Already Exists !");
                        return;
                    }            
                }
            UserAccount account = e.getUserAccountDirectory().createUserAccount(username, password, user, new UserAdminRole());
        }
        else if((EnterpriseType.getSelectedItem().toString()).equalsIgnoreCase("Insurance company")){
            for(UserAccount ua:e.getUserAccountDirectory().getUserAccounts()){
                    if(ua.getUsername().equals(username)){
                        JOptionPane.showMessageDialog(null,"This User Name Already Exists !");
                        return;
                    }            
                }
            UserAccount account = e.getUserAccountDirectory().createUserAccount(username, password, user, new InsuranceAdminRole());
        }
        populateTable();
        
        JOptionPane.showMessageDialog(null,"Enterprise successfully registered!");
        nameTextField.setText("");
        UserNameTextField.setText("");
        Password.setText(null);
        RePassword.setText(null);
    }
    }//GEN-LAST:event_RegisterActionPerformed

