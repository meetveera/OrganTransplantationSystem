        if (selectedRow < 0){
            return;
        }
        WorkRequest request = (WorkRequest)InsuranceCompany.getValueAt(selectedRow, 0);
        request.setReceiver(account);
        request.setStatus("approved");
        request.setMessage(request.getMessage());
