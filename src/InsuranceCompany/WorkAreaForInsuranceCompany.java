public void populateTable()
{
    DefaultTableModel model = (DefaultTableModel)InsuranceCompany.getModel();
        model.setRowCount(0);
       for(WorkRequest wr:insuranceAgent.getClaimMoney().getWorkRequestList()){
        Object[] row = new Object[3];
            row[0] = wr;
            row[1] =wr.getMessage();
            row[2] = wr.getStatus();
            model.addRow(row);        
        }      
}
